package Googol.Multicast;
/**
 * Enum representing different types of multicast messages.
 */
public enum MessageType {
    /**
     * Title message type.
     */
    TITLE,
    /**
     * Solicitation message type.
     */
    CITATION,
    /**
     * Token message type.
     */
    TOKENS,
    /**
     * Connection message type.
     */
    CONNECTIONS,
    /**
     * Confirmation message type.
     */
    CONFIRMATION
}
